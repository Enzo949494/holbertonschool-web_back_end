#!/usr/bin/env python3
"""Module that provides a function to concatenate two strings."""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the result.

    Args:
        str1 (str): 1st string.
        str2 (str): 2nd string.

    Returns:
        str: Concatenated string (str1 + str2).
    """
    return str1 + str2
