#!/usr/bin/env python3
"""Module that provides a function to add two floats."""


def add(a: float, b: float) -> float:
    """
    Add two floating point numbers and return their sum.

    Args:
        a (float): 1st number to add.
        b (float): 2nd number to add.

    Returns:
        float: Sum of a and b.
    """
    return a + b
